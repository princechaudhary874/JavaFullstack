package week3.day2;

public class Looping1 {
	public static void main(String[] args) {
		// display your name 1 time(sSystem.out.println("Prince Chaudhary");
		System.out.println("Prince Chaudhary");
		// Looping Statement
		// design loop
		// start_no=1
		// stop_no=10
		// statement(s)//task to do
		// Modifier(increment/decrement)

		// Print 10 to 1
		int start =10;//start
		int stop=1;//stop
		while(start>=stop) {//condition
			System.out.println(start);//statement
			start--;//modifier ->start-=1
		}
	}
}
